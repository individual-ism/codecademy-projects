int end_game(std::string ans, std::string cod) {
  if (ans == cod) {
    std::cout << "Hooray! You saved the person and earned a medal of honor!";
    return 0;
  }
  else {
    std::cout << "Oh no! The UFO just flew away with another person!";
    return 0;
  }
}

void display_status(std::vector<char> incorr, std::string ans) {
  std::cout << "\nIncorrect Guesses: \n";
    for (int i = 0; i < incorr.size(); i++) {
      std::cout << incorr[i] << " ";
    }
    std::cout << "\nCodeword: \n";
    for (int j = 0; j < ans.length(); j++) {
      std::cout << ans[j] << " ";
    }
}

